#include<bits/stdc++.h>
#define ll long long
#define _C(_0,_1,_2,_3,_4,_5,n,...) n
#define C(...) _C( , ## __VA_ARGS__ ,5,4,3,2,1,0)

int main(){
    std::cout<<C()<<'\n';
    std::cout<<C(1)<<'\n';
    std::cout<<C(1,2)<<"\n";
    std::cout<<C(1,2,3,4,5)<<"\n";
}